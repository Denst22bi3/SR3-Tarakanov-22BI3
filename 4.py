A=int(input('Введите количество лет, которые вы хотите потратить на просмотр экспонатов'))
quant=A*8*60/5*365 #расчеты сделаны по формуле A*8(часов в день)*60(минут в часе)/5(минут на 1 экспонат)*365(дней в году)
print("за это время вы просмотрите ", quant, "экспонатов")
B=int(input('Теперь введите количество экспонатов, которые вы хотите посмотреть'))
years=B/8/60*5/365 #расчеты сделаны по формуле A/8(часов в день)/60(минут в часе)*5(минут на 1 экспонат)/365(дней в году)
print("на просмотр ", B, "экспонатов вы потратите ", years, "лет")
